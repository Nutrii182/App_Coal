class Cita {
  String nombre;
  String usuario;
  String motivo;
  String fecha;
  String hora;
  String estado;

  Cita(String nombre, String usuario, String motivo, String fecha, String hora,
      String estado) {
    this.nombre = nombre;
    this.usuario = usuario;
    this.motivo = motivo;
    this.fecha = fecha;
    this.hora = hora;
    this.estado = estado;
  }
  
}
